# CONDICIONAIS COM PYTHON




# O comando se - então - senão  é feito de forma identada para separar o fluxo de instrução do resto do programa

x = 5

if x > 0 :
    print(x, " é maior que zero.")
else: print (x, " é menor que zero.")

